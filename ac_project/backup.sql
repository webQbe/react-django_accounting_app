CREATE MATERIALIZED VIEW public.mv_jl_agg_period AS
 WITH jl_norm AS (
         SELECT jl.id,
            jl.description,
            jl.debit_original,
            jl.credit_original,
            jl.is_posted,
            jl.account_id,
            jl.bank_transaction_id,
            jl.bill_id,
            jl.company_id,
            jl.fixed_asset_id,
            jl.invoice_id,
            jl.journal_id,
            jl.credit_local,
            jl.currency_id,
            jl.debit_local,
            jl.fx_rate,
            je.period_id,
            je.date AS txn_date,
            COALESCE(jl.fx_rate, 1.0) AS fxr
           FROM (public.accounts_core_journalline jl
             JOIN public.accounts_core_journalentry je ON ((je.id = jl.journal_id)))
          WHERE ((je.status)::text = 'posted'::text)
        )
 SELECT jl_norm.company_id,
    jl_norm.period_id,
    max(jl_norm.txn_date) AS last_txn_date,
    a.id AS account_id,
    a.code AS account_code,
    a.name AS account_name,
    a.ac_type AS account_type,
    sum(jl_norm.debit_original) AS total_debit_original,
    sum(jl_norm.credit_original) AS total_credit_original,
    (sum(jl_norm.debit_original) - sum(jl_norm.credit_original)) AS net_amount_original,
    sum((jl_norm.debit_original * jl_norm.fxr)) AS total_debit_local,
    sum((jl_norm.credit_original * jl_norm.fxr)) AS total_credit_local,
    (sum((jl_norm.debit_original * jl_norm.fxr)) - sum((jl_norm.credit_original * jl_norm.fxr))) AS net_amount_local
   FROM (jl_norm
     JOIN public.accounts_core_account a ON ((a.id = jl_norm.account_id)))
  GROUP BY jl_norm.company_id, jl_norm.period_id, a.id, a.code, a.name, a.ac_type
  WITH NO DATA;


