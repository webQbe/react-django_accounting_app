--
-- Name: mv_pl_period; Type: MATERIALIZED VIEW; Schema: public; Owner: sdishan
--

CREATE MATERIALIZED VIEW public.mv_pl_period AS
 SELECT m.company_id,
    m.period_id,
    sum(
        CASE
            WHEN ((m.account_type)::text = 'Income'::text) THEN m.net_amount_original
            ELSE (0)::numeric
        END) AS total_income_original,
    sum(
        CASE
            WHEN ((m.account_type)::text = 'Expense'::text) THEN m.net_amount_original
            ELSE (0)::numeric
        END) AS total_expense_original,
    (sum(
        CASE
            WHEN ((m.account_type)::text = 'Income'::text) THEN m.net_amount_original
            ELSE (0)::numeric
        END) - sum(
        CASE
            WHEN ((m.account_type)::text = 'Expense'::text) THEN m.net_amount_original
            ELSE (0)::numeric
        END)) AS net_profit_original,
    sum(
        CASE
            WHEN ((m.account_type)::text = 'Income'::text) THEN m.net_amount_local
            ELSE (0)::numeric
        END) AS total_income_local,
    sum(
        CASE
            WHEN ((m.account_type)::text = 'Expense'::text) THEN m.net_amount_local
            ELSE (0)::numeric
        END) AS total_expense_local,
    (sum(
        CASE
            WHEN ((m.account_type)::text = 'Income'::text) THEN m.net_amount_local
            ELSE (0)::numeric
        END) - sum(
        CASE
            WHEN ((m.account_type)::text = 'Expense'::text) THEN m.net_amount_local
            ELSE (0)::numeric
        END)) AS net_profit_local
   FROM public.mv_jl_agg_period m
  WHERE ((m.account_type)::text = ANY ((ARRAY['Income'::character varying, 'Expense'::character varying])::text[]))
  GROUP BY m.company_id, m.period_id
  WITH NO DATA;


