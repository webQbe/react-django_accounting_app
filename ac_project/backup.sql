-- Name: mv_trial_balance_running; Type: MATERIALIZED VIEW; Schema: public; Owner: sdishan
--

CREATE MATERIALIZED VIEW public.mv_trial_balance_running AS
 WITH jl_norm AS (
         SELECT jl.id,
            jl.description,
            jl.debit_original,
            jl.credit_original,
            jl.is_posted,
            jl.account_id,
            jl.bank_transaction_id,
            jl.bill_id,
            jl.company_id,
            jl.fixed_asset_id,
            jl.invoice_id,
            jl.journal_id,
            jl.credit_local,
            jl.currency_id,
            jl.debit_local,
            jl.fx_rate,
            COALESCE(jl.fx_rate, 1.0) AS fxr
           FROM (public.accounts_core_journalline jl
             JOIN public.accounts_core_journalentry je ON ((je.id = jl.journal_id)))
          WHERE ((je.status)::text = 'posted'::text)
        )
 SELECT jl_norm.company_id,
    a.id AS account_id,
    a.code AS account_code,
    a.name AS account_name,
    a.ac_type AS account_type,
    sum(jl_norm.debit_original) AS total_debit_to_date_original,
    sum(jl_norm.credit_original) AS total_credit_to_date_original,
    (sum(jl_norm.debit_original) - sum(jl_norm.credit_original)) AS balance_to_date_original,
    sum((jl_norm.debit_original * jl_norm.fxr)) AS total_debit_local,
    sum((jl_norm.credit_original * jl_norm.fxr)) AS total_credit_local,
    (sum((jl_norm.debit_original * jl_norm.fxr)) - sum((jl_norm.credit_original * jl_norm.fxr))) AS balance_to_date_local
   FROM (jl_norm
     JOIN public.accounts_core_account a ON ((a.id = jl_norm.account_id)))
  GROUP BY jl_norm.company_id, a.id, a.code, a.name, a.ac_type
  WITH NO DATA;


